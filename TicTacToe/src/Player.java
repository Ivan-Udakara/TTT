class Player{
    String name;
    int played;
    int won;
    int defeted;

    public int getDefeted() {
        return defeted;
    }

    public void setDefeted(int defeted) {
        this.defeted = defeted;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPlayed() {
        return played;
    }

    public void setPlayed(int played) {
        this.played = played;
    }

    public int getWon() {
        return won;
    }

    public void setWon(int won) {
        this.won = won;
    }
}